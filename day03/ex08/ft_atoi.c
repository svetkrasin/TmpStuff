int		ft_atoi(char *str)
{
	int		a;
	int		sign;

	while (*str != '\0' && !(*str >= '0' && *str <= 9))
		str++;
	str--;
	sign = 1;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else
		str++;
	a = 0;
	while ((*str >= '0' && *str <= '9') && *str != '\0')
	{
		a *= 10;
		a += *str - '0';
		str++;
	}
	return (a * sign);
}
