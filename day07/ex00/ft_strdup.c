#include <stdlib.h>

int		ft_strlen(char *s)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *duplicate;

	duplicate = (char*)malloc((ft_strlen(src) + 1) * sizeof(char));
	duplicate = ft_strcpy(duplicate, src);
	return (duplicate);
}
