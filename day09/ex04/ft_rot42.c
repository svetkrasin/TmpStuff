char	*ft_rot42(char *str)
{
	char *s;

	s = str;
	while (*str)
	{
		if (!(*str < 65 || (*str > 90 && *str < 97) || *str > 122))
		{
			if (*str >= 'a' && *str <= 'z')
				*str = (((*str - 'a' + 42) % 26) + 'a');
			else if (*str >= 'A' && *str <= 'Z')
				*str = (((*str - 'A' + 42) % 26) + 'A');
		}
		str++;
	}
	return (s);
}
